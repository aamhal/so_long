#include "so_long.h"

int moves(int key, t_sl *sl)
{
	if (key == 124)
		{
			sl->map[sl->py][sl->px] = '0';
			sl->px += 1;
			sl->map[sl->py][sl->px] = 'P';
		}
	return (0);
}